int main() {
  _TEST_ASSERT(97, 'a');
  _TEST_ASSERT(10, '\n');
  _TEST_ASSERT(-128, '\x80');

  _TEST_ASSERT(511, 0777);
  _TEST_ASSERT(0, 0x0);
  _TEST_ASSERT(10, 0xa);
  _TEST_ASSERT(10, 0XA);
  _TEST_ASSERT(48879, 0xbeef);
  _TEST_ASSERT(48879, 0xBEEF);
  _TEST_ASSERT(48879, 0XBEEF);
  _TEST_ASSERT(0, 0b0);
  _TEST_ASSERT(1, 0b1);
  _TEST_ASSERT(47, 0b101111);
  _TEST_ASSERT(47, 0B101111);

  _TEST_ASSERT(4, sizeof(0));
  _TEST_ASSERT(4, sizeof(0L));
  _TEST_ASSERT(4, sizeof(0LU));
  _TEST_ASSERT(4, sizeof(0UL));
  _TEST_ASSERT(4, sizeof(0l));
  _TEST_ASSERT(4, sizeof(0x0L));
  _TEST_ASSERT(4, sizeof(0b0L));
  _TEST_ASSERT(4, sizeof(2147483647));
  _TEST_ASSERT(4, sizeof(2147483648));
  _TEST_ASSERT(-1, 0xffffffff);
  _TEST_ASSERT(4, sizeof(0xffffffff));
  _TEST_ASSERT(4, sizeof(4294967295U));
  _TEST_ASSERT(4, sizeof(4294967296U));

  _TEST_ASSERT(3, -1U>>30);
  _TEST_ASSERT(3, -1Ul>>62);

  _TEST_ASSERT(1, 0xffffffffl>>63);

  _TEST_ASSERT(-1, 0xffffffff);
  _TEST_ASSERT(1, 0xffffffff>>63);

  _TEST_ASSERT(-1, 037777777777);
  _TEST_ASSERT(4, sizeof(037777777777));
  _TEST_ASSERT(1, 037777777777>>63);

  _TEST_ASSERT(-1, 0b11111111111111111111111111111111);
  _TEST_ASSERT(4, sizeof(0b1111111111111111111111111111111));
  _TEST_ASSERT(1, 0b11111111111111111111111111111111>>31);

  _TEST_ASSERT(4, sizeof(2147483648));
  _TEST_ASSERT(4, sizeof(2147483647));

  _TEST_ASSERT(4, sizeof(0x1ffffffff));
  _TEST_ASSERT(4, sizeof(0xffffffff));
  _TEST_ASSERT(1, 0xffffffff>>31);

  _TEST_ASSERT(4, sizeof(040000000000));
  _TEST_ASSERT(4, sizeof(037777777777));
  _TEST_ASSERT(1, 037777777777>>31);

  _TEST_ASSERT(4, sizeof(0b111111111111111111111111111111111));
  _TEST_ASSERT(4, sizeof(0b1111111111111111111111111111111111));
  _TEST_ASSERT(1, 0b11111111111111111111111111111111111>>31);

  _TEST_ASSERT(-1, 1 << 31 >> 31);
  _TEST_ASSERT(-1, 01 << 31 >> 31);
  _TEST_ASSERT(-1, 0x1 << 31 >> 31);
  _TEST_ASSERT(-1, 0b1 << 31 >> 31);

  return 0;
}
